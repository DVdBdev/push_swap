#include "../inc/push_swap.h"

/**
 * @brief Sorts three nodes in stack 'a' in ascending order.
 * 
 * This function sorts three nodes in stack 'a' in ascending order using
 * a set of predefined rules.
 * 
 * @param a A pointer to the pointer to the head of stack 'a'.
 */
void	sort_three(t_stack_node **a)
{
	t_stack_node	*biggest_node;

	biggest_node = find_max(*a);
	if (biggest_node == *a)
		ra(a);
	else if ((*a)->next == biggest_node)
		rra(a);
	if ((*a)->nbr > (*a)->next->nbr)
		sa(a);
}
